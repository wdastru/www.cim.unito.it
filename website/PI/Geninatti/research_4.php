									<span style="text-transform: none;">Combined Delivery and
										Magnetic Resonance Imaging of Neural Cell Adhesion
										Molecule–Targeted Doxorubicin- Containing Liposomes in
										Experimentally Induced Kaposi's Sarcoma</span>
								</h3>
								<p style="text-align: justify;">The aim of the present study was
									to investigate whether neural cell adhesion molecule
									(NCAM)–targeted liposomes may enhance drug delivery and allow
									MRI in a severe combined immunodeficient mouse model of
									NCAM-positive Kaposi's sarcoma. NCAM-binding peptide–coated
									liposomes loaded with both doxorubicin and a lipophilic
									gadolinium (Gd) derivative were generated. NCAM-targeted
									liposomes induced an enhanced in vitro doxorubicin
									internalization within Kaposi's cells as detected by MRI with
									respect to untargeted polyethylene glycol liposomes.
									Internalization resulted in enhanced apoptosis. In vivo weekly
									administration of NCAM-targeted liposomes induced a significant
									reduction of tumor mass and vascularization and enhanced cell
									necrosis and apoptosis with respect to untargeted liposomes.
									These effects were associated with an enhanced concentration of
									doxorubicin within the tumor and a reduced systemic toxicity of
									doxorubicin. By electron microscopy, NCAM-targeted liposomes
									were detected mainly within tumor cells whereas the untargeted
									liposomes were mainly accumulated in the extracellular space.</p>
