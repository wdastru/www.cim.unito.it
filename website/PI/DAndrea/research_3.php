										<p align="justify">
										This class of peptides was designed to mimic the &beta;-hairpin region 79-93 of VEGF. We found that peptide HPLW showed an interesting 
										VEGF-like biological activity 
										<a class="html" href="https://www.ncbi.nlm.nih.gov/pubmed/21969375"
												target="_blank"><font color="blue">[Diana et al 2011]</font></a>. 
										HPLW in solution is a monomer and assumes a stable 
										&beta;-hairpin structure as determined by NMR. The binding mode with VEGFRs was analyzed by NMR, either in vitro and in presence of 
										endothelial cells. 15N-edited NMR spectroscopic experiments demonstrate that an interaction between HPLW and VEGFR2 occurs on the 
										surface of ECs. Furthermore, competition experiments in presence of VEGF, confirmed the specificity of the interaction 
										<a class="html" href="https://www.ncbi.nlm.nih.gov/pubmed/25378243"
												target="_blank"><font color="blue">[Diana et al 2015]</font></a>. 
										Finally, thermal folding pathway of HPLW was investigated combining DSC, NMR and MD simulation 
										<a class="html" href="https://www.ncbi.nlm.nih.gov/pubmed/26602442"
												target="_blank"><font color="blue">[Diana et al 2015]</font></a>.
										The biological characterization showed that peptide HPLW is able to bind specifically VEGFRs on the 
										surface of ECs. Peptide binding to ECs induces VEGFR2 phosphorylation and activation of intracellular pathways correlated with EC survival 
										and proliferation. HPLW, as VEGF, is able to rescue ECs from apoptosis and induce proliferation. Finally, HPLW induces angiogenesis in mice 
										using an in vivo model of angiogenesis. Overall, HPLW is a proangiogenic peptide which potential in therapeutic angiogenesis. 
										A novel protease-resistant HPLW analogue is in preparation.
											
