								<h3 font style="text-transform: none;">Design and synthesis of VEGF/VEGF receptors targeting compounds</h3>
								
								<p align="justify">The Vascular Endothelial Growth Factor (VEGF) is the main regulator of angiogenesis and lymphangiogenesis, 
								both in physiological and in pathological conditions. VEGF exerts its biological function through the interaction with three 
								tyrosine kinase receptors: VEGFR1, VEGFR2 and VEGFR3. VEGFR1 and 2 are the principal drivers of physiological and pathological
								 angiogenesis. The deregulation of the VEGF/VEGFR biological axis is correlated to the onset of several common human diseases. 
								 For instance, a pathological hypervascularization is observed in cancer, in ocular degenerative diseases and in chronic inflammations, 
								 while a defective angiogenesis is associated to ischemic and cardiovascular diseases. Thus, the development of both anti-angiogenic and 
								 pro-angiogenic drugs gained prominent attention from scientists. Furthermore, VEGF and VEGFRs targeting molecules have also been proposed 
								 as imaging agents. Starting from the crystal structure of the complex between VEGF and its receptor, we designed and characterized a 
								 series of mimetic peptides reproducing epitopes of the interacting surface of VEGF with its receptor. These new molecules are useful 
								 molecular tools able to modulate angiogenesis. We obtained bioactive peptides, targeting VEGFRs, with VEGF agonist or antagonist properties, 
								 and thus finding complementary applications in biomedicine. 
								</p>

