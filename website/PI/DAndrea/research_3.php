										<p align="justify">
										We developed peptides and proteins able to interfere with the VEGF-VEGFR complex formation as potential anti-angiogenic agents. 
										We established a recombinant expression protocol for the preparation of the region 129-229 of VEGFR1 
										<a class="html" href="https://onlinelibrary.wiley.com/doi/full/10.1002/bip.21448"
												target="_blank"><font color="blue">[Di Stasi et al 2010]</font></a>. 
										This region is the main recognition site for VEGF. The protein was expressed in good yield and it was correctly folded. A structural and biological 
										characterization showed that the protein is functional active. It binds to VEGF with a binding constant of 3 nM and it is able to inhibit EC 
										proliferation induced by VEGF. This small protein is an alternative to the high molecular weight neutralizing anti-VEGF antibodies for imaging 
										and therapeutic applications. VEGFR peptide binders were also found with anti-angiogenic activity. A 15mer helical peptide was found to block 
										VEGF effect on ECs, as well as angiogenesis and tumor progression in vivo 
										<a class="html" href="https://www.ncbi.nlm.nih.gov/pubmed/21280635"
												target="_blank"><font color="blue">[Basile et al 2011]</font></a>.
										We showed that this peptide 
										binds to VEGFR2 inhibiting VEGF-dependent VEGFR2 activation, blocking EC intracellular pathways, VEGF survival activity and EC proliferation. 
										In vivo, it reduces the VEGF-dependent angiogenesis and the growth of melanoma in a xenograft model in mice. The reduction of tumor size correlates 
										with the inhibition of tumor angiogenesis. This peptide is a candidate for the development of novel peptide-based drugs for the treatment of diseases 
										associated with excessive angiogenesis. 
										
