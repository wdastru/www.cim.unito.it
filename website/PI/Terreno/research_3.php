								<h3>
									<span style="text-transform: none;">Indocyanine Green labeling
										for optical and photoacoustic imaging of Mesenchymal Stem
										Cells after in vivo transplantation</span>
								</h3>
								<p style="text-align: justify;">
									In this activity <a class="html"
										href="https://onlinelibrary.wiley.com/doi/abs/10.1002/jbio.201800035"
										target="_blank"><span style="color: blue;">[Filippi M. et al.
											2018]</span></a>, the potential of NIRF and Photoacoustic
									imaging in cellular imaging has been assessed labeling
									Mesenchymal Stem Cells with the clinically approved dye
									indocyanine green (ICG). Labelled cells were subsequently
									transplanted in healthy mice. The in vivo study was carried out
									by locally transplanting 3.0×10<sup>5</sup> ICG-labeled MSCs
									into the gastrocnemius muscle of the right hindlimb of healthy
									C57BL/6J mice. The site of cell deposition was clearly detected
									by US imaging (B-mode, 21 MHz), as a consequence of the change
									in the acoustic impedance determined by the dense inoculated
									cell mass. The PA signal intensity generated by the ICG-MSCs
									was normalized over the local endogenous baseline recorded in
									the left hindlimb of the animal where the transplantation of
									control unlabelled cells was performed.
								</p>

								<img src="images/research_3_8.jpg" alt="" class="image zooming">

								<p style="text-align: justify;">
									The PA<sub>Enh</sub> was measured over the entire range of
									excitation wavelengths immediately after cell transplantation,
									then monitored over time, and reported as PA spectrum.
								</p>
								<img src="images/research_3_9.jpg" alt="" class="image zooming">
								<p style="text-align: justify;">
									Interestingly, immediately and 4 hours post-injection, the
									maximum peak recorded in the photoacoustic spectra was shifted
									towards high excitation wavelength values (890 and 920 nm,
									respectively), whereas from day 1 to day 4, the spectral shape
									reproduced the one observed in vitro with a maximum enhancement
									centred at around 810 nm. The normalized PA spectra acquired 7
									days post injection presented a flat shape without any
									discernable peak. After each PA acquisition, the mice underwent
									NIRF imaging in order to assess the fluorescent contrast
									enhancement (FLI<sub>Enh</sub>) produced by the transplanted
									cells. Interestingly, before progressively fading over days
									(likely due to the dye degradation and washout), the FLI<sub>Enh</sub>
									values followed an initial rising trend during the first 24 h
									after the engraftment deposition.
								</p>
								<img src="images/research_3_10.jpg" alt="" class="image zooming">
								<p style="text-align: justify;">The coincident observation of
									extremely high PA amplitudes suggests that at early time points
									the strong intermolecular interactions among ICG molecules
									contribute to the quenching of the fluorescence, but results at
									the same time into an increase of the photoacoustic effect, due
									to photothermal conversion by nonradiative decay.</p>
