							<h2 style="text-transform: none; text-align: center;">Development
								of cellular imaging procedures</h2>
							<p style="text-align: justify;">
								Tracking cells in vivo by using imaging approaches represents a
								reliable method to assess the characteristics of cell grafts and
								to monitor their fate after transplantation. In that respect,
								minimally invasive techniques with high spatial resolution are
								desirable with a view of implementing therapeutic protocols in
								the clinical ordinary. MRI is a leading imaging modality
								enabling the non-invasive visualization of cell populations and
								their movements after transplantation in living animals with
								superb resolution. In order to be detectable, cells require to
								be adequately labelled with MRI contrast agents (CAs). Due to
								their excellent imaging efficiency, the superparamagnetic iron
								oxide nanoparticles (SPIONs) are regarded as the gold standard
								in cell-labeling. However, some issues are associated with their
								contrast generation mechanism based on signal-loss (negative
								contrast). As a promising alternative, paramagnetic CAs based on
								the metal Gadolinium (Gd) create a contrast increment (positive
								contrast) in T<sub>1</sub>-weighted (T<sub>1w</sub>) images,
								overcoming several complications related to the use of SPIONs.
								Even if toxic in the form of free aqueous ion, Gadolinium is
								generally considered safe when administered as a macrocyclic
								chelate and several agents have been approved for the clinical
								use so far. Differently from magnetic nanoparticles, large
								amounts of paramagnetic small molecules have to be delivered to
								target sites in order to be detected, and several labeling
								procedures have been developed to achieve this goal. In addition
								to Gd(III)-complexes, also fluorinated nanoparticles represent a
								valuable option for cellular imaging, especially for the
								visualization of the macrophage recruitment in inflammation
								sites or to tracking dendritic cells. One of the advantages of <sup>19</sup>F-MRI
								is the possibility to quantify the signal and calculate the
								cellular content in the site of interest. Besides MRI, other
								imaging modalities can be used for tracking cells in vivo, like
								optical or photoacoustic imaging.
							</p>
