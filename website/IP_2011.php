                                <h1 style="text-align: center">Intensive Programme 2011</h1>
                                <h1 style="text-align: center">Design, Synthesis and Validation
                                of Imaging Probes</h1>
                                <br>
                                <p style="font-weight: bold; font-size: 14px">
                                    Description
                                </p>
                                <p>
                                    The purpose of the Intensive Programme (IP) in "Design,
                                    Synthesis and Validation of Imaging Probes" is to develop an
                                    in-depth and up-to-date training covering the design, synthesis
                                    and characterization of imaging probes, including their
                                    functionalization and labeling for different imaging modalities.
                                </p>
                                <p>
                                    This IP will be addressed to Master and PhD students in
                                    biology, chemistry or physics. The participation of 20
                                    Master/PhD students from the partner universities will receive
                                    a support of the European Commission - Erasmus Programme to
                                    attend the course.
                                </p>
                                <p>
                                    The Intensive Programme will be hosted at the Department of
                                    Chemistry IFM of the University of Torino in the period
                                    September 19<sup>th</sup>-30<sup>th</sup>, 2011.
                                </p>
