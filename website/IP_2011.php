                                <p style="font-weight: bold; font-size: 14px">
                                    Application
                                </p>
                                <ul>
                                    <li>The application for our Intensive Programme must be made
                                        exclusively by submitting the registration form to the
                                        corresponding local coordinator. The deadline is set at 30<sup>th</sup> of July, 2011. </li>
                                    <li>The selection procedures are undertaken by each
                                        corresponding local coordinator at their partner institutions,
                                        who will decide whether to confirm that those applicants are
                                        well prepared and motivated to attend the programme. This
                                        process will continue up to completion of the 25 available
                                        places.</li>
                                    <li>Scholarships are offered to students from the partner
                                        institutions. If you are applying for a scholarship, please
                                        notice that you will have to participate in all the 10
                                        successive days of the programme. If you are planning to
                                        attend less than 10 successive days of the programme, you are
                                        not eligible for a scholarship and your expenses cannot be
                                        reimbursed.</li>
                                    <li>In case you have been selected to participate in the IP
                                        (with/without fellowship), please submit the following
                                        document, saved as a pdf file:
                                        <ul>
                                            <li>copy of your passport (or id card for EU applicants).</li>
                                            <li>an official document certifying your enrollment to a
                                                partner University for the current academic year.</li>
                                            <li>flight electronic ticket (or a scanned copy of your
                                                flight ticket). Please notice that only tickets from/to your
                                                home institution can be reimbursed (this only applies to
                                                students who have been awarded a scholarship).</li>
                                        </ul></li>
                                </ul>
