                            <p class='indent'>
                                
                                The feasibility of <sup>19</sup>F MRI was firstly demonstrated in 1977, few years after the advent of <sup>1</sup>H MRI. Even if this technique is still mostly exploited at the preclinical level, 
                                various clinical trials are ongoing to evaluate the safety and potentiality of fluorinated probes. <sup>19</sup>F MRI is based on the naturally occurring stable fluorine isotope <sup>19</sup>F 
                                (100% natural abundance), bearing a spin of &#189;, and a gyromagnetic ratio of 40.08 MHz/T (slightly lower than the 42.58 MHz/T of 1H), resulting in 83% of the sensitivity of 1H.
                                 One of the greatest advantage of <sup>19</sup>F MRI is the almost complete absence of endogenous signal, thus allowing the detection and exact quantification of administered fluorinated 
                                 compounds. However, to spatially locate the signal unambiguously, the <sup>19</sup>F acquisition has to be overlaid to the1H MR image (Figure 1).<br />
                                 
                                  <img src="../images/research_lines/Fprobes1.jpg" style="width: 180px; height: auto;  margin: 10px;" > </img>
                                  
                                 <b>Figure 1.</b> Merging of <sup>1</sup>H (grayscale) and <sup>19</sup>F (red hotspots) MR images acquired 1h (a) or 24 h (b) post injection of PFC.<br /><br />
                                 
                                 The <sup>19</sup>F spectroscopic signature extends to a range of more than 200 ppm, thus allowing the unequivocal and simultaneous identification of different 19F-containing compounds. 
                                 Moreover, it is worth mentioning thatthe chemical shift of <sup>19</sup>F is sensitive to the molecular environment of its nucleus, due to the seven outer-shell electrons of 19F atom. 
                                 Developed probes are generally chemically and biologically inertand,in order to obtain a strong resonance peak, they must contain a high number of magnetically equivalent fluorine 
                                 atoms. In addition, favorable relaxation properties are needed, in order to achieve relatively long echo times.<br />
                                 Among the most diffuse and promising fluorinated probes, perfluorocarbon (PFC) based nanosystems can be cited. These systems are basically nanoparticles or nanoemulsions made up of
                                 perfluorurated compounds, molecules similar to common organic compounds, with all the hydrogen atoms replaced by fluorine. These systems are mainly exploited for the ex-vivo or 
                                 in-vivo labeling of immune system cells (e.g. dendritic cells, monocytes, macrophages), to visualize inflamed areas and spatio-temporally track the course of the inflammatory
                                 processes, in both qualitative and quantitative way (Figure 2). Recently, the use of PFCs has been extended also to stem cell transplantation tracking.<br />
                                   
                                  <img src="../images/research_lines/Fprobes2.jpg" style="width: 180px; height: auto;  margin: 10px;" > </img>
                                  
                                  <b>Figure 2.</b> Schematic illustrating the use of PFCs for monitoring of inflammatory processes. Flogel U et al., Circulation. 2008; 118:140-148.<br /><br />
                                  
                                  
                                 Other appealing <sup>19</sup>F MRI probes, are responsive compounds, designed to be silent in physiological conditions, but MR active in presence of specific enzymes (e.g. esterase, caspase-3,
                                  &beta;-galactosidase). In addition, many fluorinated compounds capable of detecting changes in oxygen, Na<sup>+</sup>, Ca<sup>2+</sup> and Mg<sup>2+</sup> concentrations in biological tissues have been developed. 
                                  A specific attention has been devoted to the design of pH sensitive fluorinated compounds, that must have a pKa value in the physiological range, good sensitivity and specificity 
                                  as well as low toxicity, a large chemical shift range and a sufficient intracellular uptake to provide a detectable signal.<br />
                                <sup>19</sup>F MRI can be also exploited to monitor metabolism of fluorinated drugs, such as 5-fluorouracil, floxuridine, fluoexetine. However, in order to monitor drug biodistribution and the 
                                formation of fluorinated metabolic byproducts, consistent dosage of these compounds must be administered.
                                <sup>19</sup>F containing compounds can be further employed for lung functional MRI. Basically, fluorinated gases, such as fluoropropane (C<sub>3</sub>F<sub>8</sub>), hexafluoride (SF<sub>6</sub>), 
                                tetrafluoromethane (CF<sub>4</sub>) or hexafluoroethane (C<sub>2</sub>F<sub>6</sub>), can be regarded as a safe and low-cost alternative to hyper-polarized gases, currently employed for lung fMRI.
                                Finally, in the last few years, numerous fluorinated polymers have been employed in implant materials, detectable by <sup>19</sup>F MRI, with the great advantage of the absence of susceptibility
                                 artifacts.<br /><br />
                                
                                In our group, the research in the field of <sup>19</sup>F-MRI contrast agents is mainly focused on two different systems: i) perfluorocarbon-based nanosystems for tracking immune cells in
                                 inflammatory processes (mainly in CNS); and ii) fluorinated small molecules to target specific markers of Alzheimer s Disease.
                                                                 
                                
                                
