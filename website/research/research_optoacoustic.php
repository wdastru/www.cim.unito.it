                            <p class='indent'>
                                Photoacoustic imaging (PAI) is a new biomedical imaging modality based on the use of laser-generated ultrasounds that emerged over the last
                                decade. A PA image can be considered as an ultrasound image in which the contrast depend from optical properties of the tissue, particularly
                                the optical absorption. As a consequence, it offers greater specificity than conventional ultrasound with the ability to detect, and 
                                spectroscopically distinguish, light-absorbing chromophores including endogenous molecules (e.g., hemoglobin, melanin, lipids).<br /> 
                                The ideal photoacoustic contrast agent should be characterized by a distinct absorption spectrum in the near-infrared (NIR) optical window
                                 (600&minus;1100 nm), where endogenous chromophores (e.g., hemoglobin, water) have relatively low absorption; this feature allows to maximize 
                                 penetration depth by avoiding the strong absorption of intrinsic chromophore. Moreover, also a fluorescent dye can be used in PA imaging,
                                 but a low fluorescent quantum yield is preferable and results in more efficient signal generation, as the non-radiative conversion of light
                                 energy to heat is maximize. High photo-stability is also important to ensure that spectral features are not changed by light irradiation.<br /> 
								 The photoacoustic signal is tested in vitro by means of an agar phantom and the most promising contrast agents are tested in vivo in healthy 
								 and tumor bearing mice. For those dyes that are also fluorescent, optical imaging is performed to validate the results obtained with
								  photoacoustic imaging. The following contrast agents are currently under investigation for photoacoustic imaging applications. <br />
                            </p>
							
							  <ul>
                                <li><b>Small-molecules dyes </b>
                                    <p class="indent">
                                        There is a range of small molecule dyes that absorb in the NIR window and thus can be used for PAI. The chemical basis of these dyes is a series of 
                                        conjugated double bonds and/or ring systems. The heptamethine cyanine dye, Indocyanine green (ICG), is FDA&minus;approved dyes that can be used for both 
                                        fluorescence and photoacoustic imaging (off&minus;label). Beyond these FDA approved dyes, there are others, not approved for human use, such as targeted ICG
                                         (ICG&minus;RGD), IRDye800CW, Cyanine5.5, AlexaFluor750 and metal Phthalocyanine (M&minus;Pc) that are investigated as photoacoustic probes in vitro. 
                                         
                                    </p></li>
                                    
                                <li><b>Nanoparticles and microbubbles</b>
                                    <p class="indent">
										As photoacoustic contrast agents, both metallic and organic nanoparticles are investigated. Iron/silica/gold core&minus;shell nanoparticles are synthesized 
										and tested in vivo for photoacoustic and magnetic resonance imaging of ovarian cancer. Gold nanorods (AuNRs) are characterized by an extinction 
										coefficient several orders of magnitude higher than that of small-molecule dyes due to the localized surface plasmon resonance phenomenon. 
										However, biocompatibility in vivo needs further investigation. IndoCyanine Green based nanoparticles are extensively investigated, indeed ICG is
										 encapsulated inside the pores of pegylated amino-functionalized Mesoporous Silica Nanoparticles (ICG&minus;MSNs), furthermore ICG is loaded on Solid Lipid 
										 Nanoparticles (ICG&minus;SLN) liposomes and microbubbles. The loading of dyes on nanoparticles enhanced dye solubility and photostability and prolonged 
										 circulation time increasing tumor uptake. Moreover, due to fluorescence quenching of the encapsulated dyes an enhanced photoacoustic signal is observed 
										 in vitro and in vivo. Melanin&minus;containing nanoparticles are recently synthesized and tested as photoacoustic contrast agents. 
										Another innovative application is the cell tracking via ICG labeled macrophage. 
										
