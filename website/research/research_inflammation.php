                             Bacterial infections cause significant mortality and morbidity worldwide despite the availability of antibiotic. Sepsis is a serious medical condition  characterized by 
                             deregulated systemic  inflammatory response followed by immunosuppression (Cohen J et al. Lancet Infect Dis 2015; 15:581). Epidemiological records from USA show and incidence of sepsis of 3 cases per 1,000 persons annually.
                              More than 30 million cases of sepsis worldwide per annum are estimated and despite advances in supportive care, sepsis remains associated with very high mortality rates 
                              (40-60%) (Fleischmann C et al. Am J RespirCrit Care Med 2016; 193:259; Soteller J, et al. J Crit Care 2016; 31:58). Sepsis may be caused by Gram-positive, Gram-negative and polymicrobial infection. Staphylococcus aureus and Streptococcus pneumonia are Gram-positive isolates,
                               whereas Escherichia coli, Klebsiellaspecies and Pseudomonasaeruginosa predominates among Gram-negative isolates (Annane D et al. Lancet 2005; 365:63). Blood culture diagnosis of infection is the current standard
                                for sepsis. However, positive cultures can be detected in only 20% of sepsis patients. Consequently, bacterial infections can be diagnosed only after they caused significant
                                 anatomical tissue damage, a stage at which they are challenging to treat owing to the high bacterial burden. <br />
                            As human life expectancy continues to increase, so has the number of frail and immune-compromised individuals who are susceptible to bacterial infections. A major contributor to this
                             trend is the proliferation of medical implants and devices, which are inherently vulnerable to bacterial contamination. <br />
                            A major challenge  in averting biomaterial-associated infections and sepsis is the lack of a sensitive, specific non- invasive modality to detect early-stage bacterial infections, 
                            when treatment is most effective owing to the absence of profound biofilm formation. <br />
                            Currently, only indirect imaging modalities are in clinical use, as exemplified by PET with fluoro-deoxy glucose, which visualizes increased glucose uptake by inflammatory cells 
                            (Love C. et al. Radiographics 2005; 25:1357).
                             Unfortunately, these approaches lack sufficient resolution, practicality and cannot clearly discriminate between  active bacterial infection from other pathologies such as cancer
                              and general inflammation. Therefore, clinical imaging tools that are easy to use, allow bedside monitoring, and directly target invasive bacteria are highly desirable. <br /><br />
                            
                            The purpose of our unit  is to develop smart-activatable  probes to target bacteria with a MRI contrast agent. Particularly we are interesting and to explore the use of Gd complex 
                            labeled vancomycin to specifically target and detect  Gram positive bacteria andGd-conjugated to maltohexaose, as sugar that is rapidly internalized through the bacteria specific 
                            maltodextrin transport pathway (Boss w. et al. MicrobiolMolBiol Rev 1998; 62:204) in biofilms generated in vitro (Jurciseck JA et al Jove 2011; 47:1) infections as well as in the 
                            cecal ligation and puncture model of  murine sepsis (Dejager L et al Trends in Microbiology 2011; 19:198).
                                                            
                                
                                
