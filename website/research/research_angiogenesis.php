									evaluate the response to pharmacological treatments. We mainly
									focus on DCE&minus;MRI technique, that allows the visualization
									of tumor vasculature by exploiting the injection of pre
									clinical/clinical Gd<sup>3+</sup>&minus;based contrast agents
									informative of tumor perfusion and permeability. In particular,
									we recently developed a novel
									<a
