                            <p class='indent'>
                                Angiogenesis is commonly indicated as a hallmark of cancer.  It involves the formation of new capillaries from pre-existing vessels for 
								ensuring the blood support necessary to tumor expansion and metastasis to distant organs. In cancer biology, this process is highly deregulated. 
								As a consequence, tumor vasculature displays a highly destructured and dysfunctional network, mainly characterized by hyperpermeability and reduced 
								tumor perfusion. These structural features contribute to vascular heterogeneity and impair tumor blood flow responsible of hypoxia, 
								interstitial pressure and acidosis. Therefore, several therapeutic strategies are under investigation tonormalize the tumor vasculature
								throughout antiangiogenic therapies for improving blood perfusion and for decreasing level of hypoxia that makes the cells resistant 
								to radiotherapy and cytotoxic agents. <br />
								Several imaging techniques have been developed to visualize these structural abnormalities.</p>
								
								<ul>
                                <li><b>Dynamic Contrast Enhanced (DCE) MRI</b>
                                    <p class="indent">
                           
								In our lab we develop MRI-based functional approaches
								in order to characterize tumor angiogenesis in several murine models and evaluate the response to pharmacological treatments. We mainly focus 
								on DCE&minus;MRI technique, that allows the visualization of tumor vasculature by exploiting the injection of 
								pre clinical/clinical Gd<sup>3+</sup>&minus;based contrast agents informative of tumor perfusion and permeability. In particular, we recently developed
								a novel <a href="research_gado.php" style="color:#dc6a10">blood pool agent (Gd-AAZTA Madec)</a> able to provide an accurate quantification of intratumoral vessels at low magnetic field, 
								therefore facilitating the translation of our approach. In combination with the superb anatomical resolution of MRI,
								this technique allows the implementation of dedicated tools able to analyse tumor heterogeneity and visualize its role in modulating
								the response to therapy. </p></li>
								
								<li><b>DCE-CEST (Chemical Exchange Saturation Transfer) MRI</b>
                                    <p class="indent">
								In addition, our research recently focused on the development of MRI perfusion agents able to be detected by the
