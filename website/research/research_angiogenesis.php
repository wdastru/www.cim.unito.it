						<p class='indent'>
							Angiogenesis is commonly indicated as a hallmark of cancer. It
							involves the formation of new capillaries from pre-existing
							vessels for ensuring the blood support necessary to tumor
							expansion and metastasis to distant organs. In cancer biology,
							this process is highly deregulated. As a consequence, tumor
							vasculature displays a highly destructured and dysfunctional
							network, mainly characterized by hyperpermeability and reduced
							tumor perfusion. These structural features contribute to vascular
							heterogeneity and impair tumor blood flow responsible of hypoxia,
							interstitial pressure and acidosis. Therefore, several
							therapeutic strategies are under investigation tonormalize the
							tumor vasculature throughout antiangiogenic therapies for
							improving blood perfusion and for decreasing level of hypoxia
							that makes the cells resistant to radiotherapy and cytotoxic
							agents. <br> Several imaging techniques have been developed to
							visualize these structural abnormalities.
						</p>
