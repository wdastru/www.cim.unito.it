                            <p class="indent">
                                The Clinical and Experimental Center for
                                Functional Magnetic Resonance Imaging (CCS-fMRI) was set up in
                                2004, the result of a partnership between the Department of
                                Psychology of the University of Turin and the Neuroradiology
                                Department of the Koelliker Hospital in Turin. As a research
                                facility it is also part of the Interdepartmental Center for
                                Advanced Studies in Neuroscience of the University of Turin (NIT -
                                Neuroscience Institute of Turin), and the Center for Molecular
                                Imaging of the University of Turin (CIM). The CCS-fMRI is dedicated
                                to clinical and experimental neuroscience research. Its goal is to
                                develop protocols for investigating brain connectivity using
                                functional magnetic resonance imaging, as the basis for heuristics
                                and practical guidelines.
                            </p>
