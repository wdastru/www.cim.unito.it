                            <p class="indent">
                                We use both anatomical and functional magnetic
                                resonance imaging techniques. Anatomical techniques include
                                morphometric analysis to measure cortical volume and thickness (in
                                particular, Voxel-Based Morphometry, VBM), and Diffusion Tensor
                                Imaging (DTI) to study the axonal bands and anatomical
                                connectivity. As regards functional techniques, we use functional
                                magnetic resonance imaging (fMRI) to investigate activation,
                                functional connectivity techniques such as seed voxel correlation
                                and Independent Component Analysis (ICA), and effective
                                connectivity methods such as Granger Causality Models (GCM),
                                Structural Equation Models (SEM) and Dynamic Causal Models (DCM).
                                By way of example, some of the functional magnetic resonance
                                imaging techniques in which we have expertise are described in more
                                detail below. Functional magnetic resonance imaging using
                                activation paradigms. - Motor paradigms for the upper limbs
                                (flexion and extension of the fingers; finger tapping); for the
                                lower limbs (dorsiflexion of the ankle, dorsiflexion of the toes);
                                for tongue movement. The ankle dorsiflexion test, in particular, is
                                used to investigate the brain areas involved in gait. This is
                                useful for diagnosis and for evaluating the effectiveness of
                                walking rehabilitation programs. Engineers from the Department of
                                Mechanical Engineering at Turin Polytechnic have designed and built
                                an apparatus which makes it possible to assess patients with
                                paralysis or plegia, by enabling both active and passive ankle
                                movements during MR imaging. The use of this apparatus, which is
                                capable of adjusting and measuring the force and speed of the
                                movement, also makes it possible to compare the results of pre- and
                                post-therapy tests in patients undergoing rehabilitation training.
                                In specific clinical or experimental circumstances, we use
                                paradigms of motor imagery for movements of the upper limbs or
                                walking movements without their overt execution. In research, the
                                use of such motor paradigms and motor imagery has enabled us to
                                validate protocols for gait analysis which have possible
                                implications for rehabilitation (Sacco et al., 2006; Sacco et al.,
                                2009). - Sensory paradigms (brushing of the index and little
                                fingers) are used to identify the somatosensory representation of
                                the fingers. - Visual paradigms (observation of colored shapes) are
                                used to map visual areas associated with position, form, color. -
                                Language paradigms. Of these, the paradigm we use most consists of
                                creating verbs derived from nouns. We have found this to be the
                                most appropriate paradigm for identifying the frontal and temporal
                                language regions and providing the most accurate measure of
                                hemispheric lateralization. These paradigms are mainly used in
                                pre-surgery evaluations. Working in collaboration with neurosurgery
                                departments we have, in some selected cases, been able to use a
                                language paradigm that had previously been used in resonance
                                imaging, during cortical stimulation of the areas involved in
                                language in patients undergoing awake neurosurgery (for a summary
                                of our work in this field see Spena et al., in press). - Cognitive
                                paradigms are used to explore the brain substrates of various
                                mental processes, such as attention (Amanzio et al., in press),
                                verbal and spatial memory (Caglio et al., 2010; in press),
                                recognition of primary and social emotions through facial and body
                                expressions (Tamietto et al., in preparation). Some
                                neuropsychological tests (e.g. the Benton line orientation test,
                                landmark tasks for analyzing unilateral spatial neglect) are used
                                as research tools in experiments involving specific neurological
                                populations. The motor, sensory, visual and language paradigms are
                                based on a block design, in which the activation condition
                                alternates with a baseline or lation investigated using
                                electroencephalography Another research topic is concerned with the
                                functional significance and modulation of evoked potentials,
                                measured using electroencephalography (EEG). These evoked brain
                                responses are shown as deviations in the EEG reading, and as
                                alterations in brain activity within the specific frequency ranges
                                that characterize spontaneous brain electrical activity (e.g.
                                rhythms &alpha;, &beta;, &gamma;) following the administration of
                                stimuli. One aim of our research in this area is to investigate and
                                characterize the influence of attention and cognitive conflict
                                (introduced, for instance, by the assumption of unusual postures,
                                leading to misalignment of spatial and somatotopic maps) on these
                                brain responses and their brain mapping.
