							<li><b>The fibrin target</b> <br>
								<p class="indent">We are working on a system made of a peptide
									and four Gd complexes and trying to optimize both the binding
									affinity and the contrast it induces in the MRI images. A
									number of diseases inveriably show the presence of fibrin
									deposits (tumours, plaques, trombi ...). The development of an
									efficient system for spotting these deposits could represent a
									"one-stop-shop-agent" that could be used for different
									applications.</p></li>
							<li><b>The XIII factor target</b> <br>
								<p class="indent">A previous work permitted us to identify a
									peptide which is an optimal substrate for such enzime, and give
									rise to its fast inclusion in the matrix of a forming trombus.
									This peptide could be functionalized with one or more units of
									Gd-chelate in order to obtain a forming-trombus reporter.</p></li>
							<li><b>The -SH target</b> <br>
								<p class="indent">The easy oxidation of the thiolic function
									represent an excellent indicator of the redox state of the
									biological microenvironment. Furthermore, the presence of
									exposed SH groups could be the result of mutations and
									subsequent conformational variations associated to important
									diseases (PPL/multiple sclerosis). For these reasons we are
									optimizing a probe able to specifically bind to free thiols.
									their quantitation could have interesting diagnostic
									implications.</p></li>
							<li><b>The MMPs target</b> <br>
								<p class="indent">The Matrix Metallo Proteases are enzimes that
									are present in a number of tissutal remodeling situations and
									represent important indicators of diseases (tumours, multiple
									sclerosis, atherosclerotic plaques). The probe is represented
									by insoluble particles that could be solubilized in presence of
									the enzime; by this way they transform from a T2* into a T1
									agent. The insolubility is due to the presence of a lipophilic
									chain that is separated from the Gd complex by a suitable
									spacer that could be cleaved by the MMPs. The particles are
									made of aggregates of Gd complexes, cyclodextrins, aggregating
									and driving agents (agents that have the role of driving the
									particles to the site of interest).</p></li>
