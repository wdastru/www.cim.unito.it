 Research at CCS fMRI <!-- InstanceEndEditable --></h1>
</div>
<div class='paddingInner'>
<!-- InstanceBeginEditable name="subsection content" -->
<p class="indent">The Clinical and Experimental Center for
Functional Magnetic Resonance Imaging (CCS-fMRI) was set up in
2004, the result of a partnership between the Department of
Psychology of the University of Turin and the Neuroradiology
Department of the Koelliker Hospital in Turin. As a research
facility it is also part of the Interdepartmental Center for
Advanced Studies in Neuroscience of the University of Turin (NIT -
Neuroscience Institute of Turin), and the Center for Molecular
Imaging of the University of Turin (CIM). The CCS-fMRI is dedicated
to clinical and experimental neuroscience research. Its goal is to
develop protocols for investigating brain connectivity using
functional magnetic resonance imaging, as the basis for heuristics
and practical guidelines.</p>
<p class="indent">Up until now, our research projects using the 1.5
Tesla fMRI scanner at the Koelliker Hospital have mainly been
concerned with functional assessments of brain-damaged patients and
the evaluation of the effectiveness of rehabilitation therapy.
These research projects have attracted funding from various
institutions and foundations and led to a number of scientific
publications (see below). Members of the fMRI research team from
the Koelliker Hospital also use our findings as the subject of
their lectures given at the University of Turin and at credit
courses offered by other institutions as part of the
&ldquo;Educazione Continua in Medicina&rdquo; (Continuing Medical
Education) program run by the Italian Ministry of Health. Our
research projects and training activities for members of the fMRI
research team are made possible thanks to our partnerships with
national and international clinics and research centers. Our
international partners include:</p>
<ul>
<li>Cognitive and Affective Neuroscience Laboratory, Tilburg
University, The Netherlands</li>
<li>Maastricht Brain Imaging Center, Department of Cognitive
Neuroscience, Maastricht University, The Netherlands</li>
<li>Institute of Neurology, University College London, London,
UK</li>
</ul>
<p class="indent">The university coordinator is Prof. Giuliano
Geminiani, Professor of Clinical Neuropsychology at the Department
of Psychology, University of Turin. The hospital coordinator is Dr.
Sergio Duca, head of the Neuroradiology Unit at the Koelliker
Hospital in Turin.</p>
