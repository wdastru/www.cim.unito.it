							<br />
							<p style="font-weight: bold; font-size: 14px">Description</p>
							<p>The purpose of the Intensive Programme (IP) in "Design,
								Synthesis and Validation of Imaging Probes" is to develop an
								in-depth and and up-to-date training covering the design,
								synthesis and characterization of imaging probes including their
								functionalization and labelling for different imaging
								modalities. Teaching and practical sessions with hands-on
								experiments presented and supervised by scientists of the
								coordinating institution will be the backbone of the intensive
								programme. In addition lectures and lectures with practical
								demonstration will be implemented with the contribution of
								several partners of the project.</p>
							<p>This IP will be addressed to Master and PhD students in
								biology, chemistry or physics. The partecipation of 20
								Master/PhD students from the partner universities will receive a
								support of the European Commission Eramus Programme to attend
								the course.</p>
							<p>The Intensive Programme will be hosted at the Department of
								Chemistry IFM of the University of Torino in the period
								September 7-18, 2009.</p>
